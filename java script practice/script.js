
var arr=["a","b","c","d"]
//accessing element 
console.log(arr[3])
//remove element at last index using pop
arr.pop()
console.log(arr)
//adding element at last index using push
arr.push(2)
console.log(arr)    
//removing element from first index using shift
arr.shift()
console.log(arr)
//adding element from first index using unshift
arr.unshift(200)
console.log("unshift array are",arr)
//in this we learned some basic method to add or remove or accesing index in array 